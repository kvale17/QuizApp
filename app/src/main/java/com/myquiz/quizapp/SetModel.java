package com.myquiz.quizapp;

public class SetModel {

    String id;
    String name;

    public SetModel(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

}
